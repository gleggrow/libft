#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	k;
	char	*h;

	k = 0;
	h = (char *)b;
	while (k != len)
	{
		h[k] = c;
		k++;
	}
	return (b);
}
